function run_permucorr_examples
%RUN_PERMUCORR_EXAMPLES  Run permutation correlation test examples.
%   Generates random multivariate data for 2 "dependent" samples X and Y.
%   Each sample has 20 variables, each pair with a correlation of 0, except
%   for the first 5 variables which have a positive correlation, and the
%   second 5 variables which have a negative correlation. Each variable has
%   30 observations. Permutation tests based on the Pearson and Spearman
%   correlation coefficient are performed between the corresponding
%   variables of each sample for two-tailed, right-tailed and left-tailed
%   tests. The results are compared to those of the equivalent parametric
%   statistical tests (i.e. Pearson's correlation using a Student's
%   t-distribution) using corr.m and corrcoef.m.
%
%   Note: The parametric confidence intervals (CIs) generated by corrcoef.m
%   for the right-tailed and left-tailed tests are actually based on that
%   of a two-tailed test (as there is no option to specify the test tail in
%   corrcoef.m) and are thus slightly overestimated.
%
%   See also PERMUCORR CORR CORRCOEF.
%
%   PERMUTOOLS https://github.com/mickcrosse/PERMUTOOLS

%   Â© 2018 Mick Crosse <mickcrosse@gmail.com>
%   CNL, Albert Einstein College of Medicine, NY.

% Generate random data
rng(42);
x = randn(30,20);
y = randn(30,20);
y(:,1:5) = y(:,1:5)+x(:,1:5)/2;
y(:,6:10) = y(:,6:10)-x(:,6:10);
xaxis = 1:20;
tail = {'both','right','left'};
type = {'Pearson','Spearman'};

for n = 1:numel(type)

    % Plot parametric & permutation CIs
    figure
    for i = 1:numel(tail)
        [r1,p1] = corr(x,y,'tail',tail{i},'type',type{n});
        r1 = diag(r1); p1 = diag(p1);
        ci1 = zeros(2,20);
        for j = 1:20
            switch type{n}
                case 'Pearson'
                    [rj,pj,clwr,cupr] = corrcoef(x(:,j),y(:,j));
                case 'Spearman'
                    [rj,pj,clwr,cupr] = corrcoef(tiedrank(x(:,j)),...
                        tiedrank(y(:,j)));
            end

            switch tail{i}
                case 'right'
                    ci1(:,j) = [clwr(2);Inf];
                case 'left'
                    ci1(:,j) = [-Inf;cupr(2)];
                otherwise
                    ci1(:,j) = [clwr(2);cupr(2)];
            end
        end
        [r2,p2,ci2] = permucorr(x,y,'tail',tail{i},'type',type{n},...
            'correct',false);
        subplot(3,2,i+i-1)
        hold on
        plot(xaxis,r2,'LineWidth',3)
        plot(xaxis,ci1,'k',xaxis,ci2,'--r')
        plot(xaxis(p1<=0.05),r1(p1<=0.05),'ok','LineWidth',2)
        plot(xaxis(p2<=0.05),r2(p2<=0.05),'xr','LineWidth',2)
        hold off
        ylim([-2,2])
        xlim([0,21])
        if i == 1
            title('Uncorrected')
        elseif i == 3
            xlabel('variable')
        end
        ylabel([tail{i},'-tailed'])
        if i == 2
            legend([type{n},' corr.'],'parametric CIs','',...
                'permutation CIs')
        end
        [r2,p2,ci2] = permucorr(x,y,'tail',tail{i},'type',type{n},...
            'correct',true);
        subplot(3,2,i+i)
        hold on
        plot(xaxis,r2,'LineWidth',3)
        plot(xaxis,ci1,'k',xaxis,ci2,'--r')
        plot(xaxis(p1<=0.05),r1(p1<=0.05),'ok','LineWidth',2)
        plot(xaxis(p2<=0.05),r2(p2<=0.05),'xr','LineWidth',2)
        hold off
        ylim([-2,2])
        xlim([0,21])
        if i == 1
            title('Corrected')
        elseif i == 3
            xlabel('variable')
        end
    end

    % Plot parametric & permutation p-values
    figure
    for i = 1:numel(tail)
        [~,p1] = corr(x,y,'tail',tail{i},'type',type{n});
        p1 = diag(p1);
        [~,p2] = permucorr(x,y,'tail',tail{i},'type',type{n},...
            'correct',false);
        subplot(3,2,i+i-1)
        hold on
        plot(xaxis,p1,'k',xaxis,p2,'--r','LineWidth',2)
        plot(xaxis(p1<=0.05),p1(p1<=0.05),'ok','LineWidth',2)
        plot(xaxis(p2<=0.05),p2(p2<=0.05),'xr','LineWidth',2)
        hold off
        ylim([0,1])
        xlim([0,21])
        if i == 1
            title('Uncorrected')
        elseif i == 3
            xlabel('variable')
        end
        ylabel([tail{i},'-tailed'])
        if i == 2
            legend('parametric p-value','permutation p-value')
        end
        [r2,p2] = permucorr(x,y,'tail',tail{i},'type',type{n},...
            'correct',true);
        subplot(3,2,i+i)
        hold on
        plot(xaxis,p1,'k',xaxis,p2,'--r','LineWidth',2)
        plot(xaxis(p1<=0.05),p1(p1<=0.05),'ok','LineWidth',2)
        plot(xaxis(p2<=0.05),p2(p2<=0.05),'xr','LineWidth',2)
        hold off
        ylim([0,1])
        xlim([0,21])
        if i == 1
            title('Corrected')
        elseif i == 3
            xlabel('variable')
        end
    end

end